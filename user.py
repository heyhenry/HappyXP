class UserInfo:
    def __init__(self, display_name, username, password, toggle_login, bio_message, total_entries_count, total_chapters_count, total_episodes_count, total_anime_count, total_manga_count, achievements):
        self.display_name = display_name
        self.username = username
        self.password = password
        self.toggle_login = toggle_login
        self.bio_message = bio_message
        self.total_entries_count = total_entries_count
        self.total_chapters_count = total_chapters_count
        self.total_episodes_count = total_episodes_count
        self.total_anime_count = total_anime_count
        self.total_manga_count = total_manga_count
        self.achievements = achievements

